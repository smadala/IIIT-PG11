#!/bin/bash

# * can be used to match everything and even null
echo "Showing all files and directories in current folder"
echo *
echo
echo


# * can be used as multiplication operator
A=$((3 * 4))
echo "3 * 4 = $A"
echo
echo


# ** can be used as exponentiation operator
B=$((2 ** 3))
echo "2 raised to power 3 is $B"
echo
echo

exit 0

